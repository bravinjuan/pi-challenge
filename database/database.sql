CREATE TABLE "Ventas" (
	"id"	INTEGER PRIMARY KEY AUTOINCREMENT,
	"fecha" DATE NOT NULL,
	"monto" FLOAT NOT NULL,
	"cliente" VARCHAR(255) NOT NULL
);

CREATE TABLE "Articulos" (
	"id" INTEGER PRIMARY KEY AUTOINCREMENT,
	"nombre" VARCHAR(255) NOT NULL,
	"precio" FLOAT NOT NULL,
	"stock" INTEGER NOT NULL
);